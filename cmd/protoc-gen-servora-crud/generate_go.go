package main

import (
	"fmt"
	"strings"

	"google.golang.org/protobuf/compiler/protogen"
)

var (
	fmtImport          = protogen.GoImportPath("fmt")
	stringsImport      = protogen.GoImportPath("strings")
	protoreflectImport = protogen.GoImportPath("google.golang.org/protobuf/reflect/protoreflect")
)

func generateGoFile(gen *protogen.Plugin, file *protogen.File, resources []*resourceInfo) {
	filename := file.GeneratedFilenamePrefix + "_crud.pb.go"
	generated := gen.NewGeneratedFile(filename, file.GoImportPath)
	generated.P("// Code generated by protoc-gen-servora-crud. DO NOT EDIT.")
	generated.P()
	generated.P("package ", file.GoPackageName)
	generated.P()
	for _, resource := range resources {
		generateGoResource(generated, resource)
	}
}

func generateGoResource(g *protogen.GeneratedFile, resource *resourceInfo) {
	name := resource.message.GoIdent.GoName
	fieldPath := name + "FieldPath"
	g.P("// ", name, "CRUDDescriptor returns the official protobuf descriptor for ", name, ".")
	g.P("func ", name, "CRUDDescriptor() ", qualified(g, protoreflectImport, "MessageDescriptor"), " {")
	g.P("return (&", name, "{}).ProtoReflect().Descriptor()")
	g.P("}")
	g.P()
	g.P("// ", fieldPath, " identifies one canonical protobuf field path and resolves its leaf descriptor lazily.")
	g.P("type ", fieldPath, " struct { path string }")
	g.P()
	g.P("// String returns the canonical protobuf snake_case path.")
	g.P("func (path ", fieldPath, ") String() string { return path.path }")
	g.P("// Descriptor returns the path leaf descriptor.")
	g.P("func (path ", fieldPath, ") Descriptor() ", qualified(g, protoreflectImport, "FieldDescriptor"), " { return resolve", name, "FieldPath(path.path) }")
	g.P()
	g.P("func resolve", name, "FieldPath(path string) ", qualified(g, protoreflectImport, "FieldDescriptor"), " {")
	g.P("descriptor := ", name, "CRUDDescriptor()")
	g.P("parts := ", qualified(g, stringsImport, "Split"), "(path, \".\")")
	g.P("var leaf ", qualified(g, protoreflectImport, "FieldDescriptor"))
	g.P("for index, part := range parts {")
	g.P("leaf = descriptor.Fields().ByName(", qualified(g, protoreflectImport, "Name"), "(part))")
	g.P("if leaf == nil { panic(", qualified(g, fmtImport, "Sprintf"), "(\"generated CRUD field path %q is missing\", path)) }")
	g.P("if index < len(parts)-1 {")
	g.P("if leaf.Message() == nil || leaf.IsList() || leaf.IsMap() { panic(", qualified(g, fmtImport, "Sprintf"), "(\"generated CRUD field path %q traverses a non-message field\", path)) }")
	g.P("descriptor = leaf.Message()")
	g.P("}")
	g.P("}")
	g.P("return leaf")
	g.P("}")
	g.P()
	g.P("// ", name, "Fields contains every supported resource field path.")
	g.P("var ", name, "Fields = struct {")
	for _, field := range resource.fields {
		g.P(field.symbol, " ", fieldPath)
	}
	g.P("}{")
	for _, field := range resource.fields {
		g.P(field.symbol, ": ", fieldPath, "{path: ", fmt.Sprintf("%q", field.path), "},")
	}
	g.P("}")
	g.P()
	generateGoNameHelper(g, resource)
}

func generateGoNameHelper(g *protogen.GeneratedFile, resource *resourceInfo) {
	name := resource.message.GoIdent.GoName
	nameType := name + "Name"
	multiPattern := len(resource.patterns) > 1
	if multiPattern {
		g.P("type ", lowerFirst(nameType), "Pattern uint8")
		g.P("const (")
		g.P(lowerFirst(nameType), "PatternInvalid ", lowerFirst(nameType), "Pattern = iota")
		for index := range resource.patterns {
			g.P(lowerFirst(nameType), "Pattern", index+1)
		}
		g.P(")")
		g.P()
	}
	g.P("// ", nameType, " is the typed parts value for a canonical ", name, " resource name.")
	g.P("type ", nameType, " struct {")
	for _, variable := range resource.variables {
		g.P(goExportedName(variable), " string")
	}
	if multiPattern {
		g.P("pattern ", lowerFirst(nameType), "Pattern")
	}
	g.P("}")
	g.P()
	if multiPattern {
		for index, pattern := range resource.patterns {
			constructor := "New" + nameType + "For" + pattern.constructorSuffix
			generateGoNameConstructor(g, constructor, nameType, pattern, fmt.Sprintf("%sPattern%d", lowerFirst(nameType), index+1))
		}
	} else {
		generateGoNameConstructor(g, "New"+nameType, nameType, resource.patterns[0], "")
	}
	g.P("// Parse", nameType, " parses one canonical resource name.")
	g.P("func Parse", nameType, "(value string) (", nameType, ", error) {")
	g.P("parts := ", qualified(g, stringsImport, "Split"), "(value, \"/\")")
	for index, pattern := range resource.patterns {
		g.P("if len(parts) == ", len(pattern.segments), " {")
		conditions := make([]string, 0, len(pattern.segments))
		for segmentIndex, segment := range pattern.segments {
			if segment.literal != "" {
				conditions = append(conditions, fmt.Sprintf("parts[%d] == %q", segmentIndex, segment.literal))
			}
		}
		if len(conditions) == 0 {
			g.P("if true {")
		} else {
			g.P("if ", strings.Join(conditions, " && "), " {")
		}
		g.P("candidate := ", nameType, "{")
		for segmentIndex, segment := range pattern.segments {
			if segment.variable != "" {
				g.P(goExportedName(segment.variable), ": parts[", segmentIndex, "],")
			}
		}
		if multiPattern {
			g.P("pattern: ", lowerFirst(nameType), "Pattern", index+1, ",")
		}
		g.P("}")
		g.P("if err := candidate.Validate(); err == nil { return candidate, nil }")
		g.P("}")
		g.P("}")
	}
	g.P("return ", nameType, "{}, ", qualified(g, fmtImport, "Errorf"), "(\"invalid ", name, " resource name %q\", value)")
	g.P("}")
	g.P()
	g.P("// Validate checks the selected pattern and all variable segments.")
	g.P("func (name ", nameType, ") Validate() error {")
	if multiPattern {
		g.P("switch name.pattern {")
		for index, pattern := range resource.patterns {
			g.P("case ", lowerFirst(nameType), "Pattern", index+1, ":")
			generateGoNameValidation(g, name, resource.variables, pattern)
		}
		g.P("default:")
		g.P("return ", qualified(g, fmtImport, "Errorf"), "(\"", name, " resource name pattern is not selected\")")
		g.P("}")
	} else {
		generateGoNameValidation(g, name, resource.variables, resource.patterns[0])
	}
	g.P("}")
	g.P()
	g.P("// Format validates and formats an unescaped canonical relative resource name.")
	g.P("func (name ", nameType, ") Format() (string, error) {")
	g.P("if err := name.Validate(); err != nil { return \"\", err }")
	if multiPattern {
		g.P("switch name.pattern {")
		for index, pattern := range resource.patterns {
			g.P("case ", lowerFirst(nameType), "Pattern", index+1, ":")
			generateGoNameFormatReturn(g, pattern)
		}
		g.P("default:")
		g.P("return \"\", ", qualified(g, fmtImport, "Errorf"), "(\"", name, " resource name pattern is not selected\")")
		g.P("}")
	} else {
		generateGoNameFormatReturn(g, resource.patterns[0])
	}
	g.P("}")
	g.P()
}

func generateGoNameConstructor(
	g *protogen.GeneratedFile,
	constructor, nameType string,
	pattern namePattern,
	patternValue string,
) {
	parameters := make([]string, 0, len(pattern.variables))
	for _, variable := range pattern.variables {
		parameters = append(parameters, variable+" string")
	}
	g.P("// ", constructor, " constructs the parts for pattern ", fmt.Sprintf("%q", pattern.raw), ".")
	g.P("func ", constructor, "(", strings.Join(parameters, ", "), ") ", nameType, " {")
	g.P("return ", nameType, "{")
	for _, variable := range pattern.variables {
		g.P(goExportedName(variable), ": ", variable, ",")
	}
	if patternValue != "" {
		g.P("pattern: ", patternValue, ",")
	}
	g.P("}")
	g.P("}")
	g.P()
}

func generateGoNameValidation(
	g *protogen.GeneratedFile,
	resourceName string,
	allVariables []string,
	pattern namePattern,
) {
	used := make(map[string]struct{}, len(pattern.variables))
	for _, variable := range pattern.variables {
		used[variable] = struct{}{}
		field := goExportedName(variable)
		g.P("if name.", field, " == \"\" || ", qualified(g, stringsImport, "Contains"), "(name.", field, ", \"/\") {")
		g.P("return ", qualified(g, fmtImport, "Errorf"), "(\"", resourceName, " resource name variable ", variable, " must be non-empty and contain no slash\")")
		g.P("}")
	}
	for _, variable := range allVariables {
		if _, ok := used[variable]; ok {
			continue
		}
		field := goExportedName(variable)
		g.P("if name.", field, " != \"\" { return ", qualified(g, fmtImport, "Errorf"), "(\"", resourceName, " resource name variable ", variable, " does not belong to the selected pattern\") }")
	}
	g.P("return nil")
}

func generateGoNameFormatReturn(g *protogen.GeneratedFile, pattern namePattern) {
	parts := make([]string, 0, len(pattern.segments))
	for _, segment := range pattern.segments {
		if segment.literal != "" {
			parts = append(parts, fmt.Sprintf("%q", segment.literal))
		} else {
			parts = append(parts, "name."+goExportedName(segment.variable))
		}
	}
	g.P("return ", qualified(g, stringsImport, "Join"), "([]string{", strings.Join(parts, ", "), "}, \"/\"), nil")
}

func qualified(g *protogen.GeneratedFile, importPath protogen.GoImportPath, name string) string {
	return g.QualifiedGoIdent(protogen.GoIdent{GoName: name, GoImportPath: importPath})
}

func lowerFirst(value string) string {
	if value == "" {
		return value
	}
	return strings.ToLower(value[:1]) + value[1:]
}
